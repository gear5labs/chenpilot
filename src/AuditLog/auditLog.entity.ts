// ─── Entity ───────────────────────────────────────────────────────────────────

/**
 * Composite indexes are ordered for SecOps forensic query patterns:
 *
 *   (category, createdAt)        — enumerate all events in a bucket over time
 *   (correlationId, createdAt)   — reconstruct a full distributed trace
 *   (actorId, createdAt)         — actor-centric timeline
 *   (action, createdAt)          — action-specific reporting
 *   (severity, createdAt)        — alert-severity dashboards
 *   (userId, createdAt)          — user timeline (legacy compat)
 *   createdAt (standalone)       — range scans / retention purge
 */
@Entity("audit_log")
@Index("idx_audit_category_time", ["category", "createdAt"])
@Index("idx_audit_correlation_time", ["correlationId", "createdAt"])
@Index("idx_audit_actor_time", ["actorId", "createdAt"])
@Index("idx_audit_action_time", ["action", "createdAt"])
@Index("idx_audit_severity_time", ["severity", "createdAt"])
@Index("idx_audit_user_time", ["userId", "createdAt"])
export class AuditLog {
  // ── Primary Key ────────────────────────────────────────────────────────────

  @PrimaryGeneratedColumn("uuid")
  id!: string;

  // ── Distributed Tracing ────────────────────────────────────────────────────

  /**
   * Correlation ID propagated from the inbound HTTP header
   * (`x-correlation-id` or `x-request-id`).  Ties every downstream
   * audit event generated by one user request together.
   */
  @Column({ type: "varchar", nullable: true })
  @Index("idx_audit_correlation_id")
  correlationId?: string;

  // ── Actor (who) ────────────────────────────────────────────────────────────

  /** Authenticated user ID — may be null for anonymous / service actions */
  @Column({ type: "varchar", nullable: true })
  @Index("idx_audit_user_id")
  userId?: string;

  /** Canonical actor ID — mirrors userId for human actors, serviceId otherwise */
  @Column({ type: "varchar", nullable: true })
  @Index("idx_audit_actor_id")
  actorId?: string;

  /** Service / bot identifier for non-human actors */
  @Column({ type: "varchar", nullable: true })
  actorServiceId?: string;

  /** Roles held by the actor at the time of the event */
  @Column({ type: "simple-array", nullable: true })
  actorRoles?: string[];

  // ── Action + Category ──────────────────────────────────────────────────────

  /** Typed action string, e.g. "auth.login.success" */
  @Column({ type: "varchar" })
  action!: string;

  /** Taxonomy bucket: Auth | Admin | Execution | Policy | Integration */
  @Column({
    type: "varchar",
    nullable: true,
    default: null,
  })
  category?: EventCategory;

  // ── Severity + Outcome ─────────────────────────────────────────────────────

  @Column({ type: "varchar", default: AuditSeverity.INFO })
  severity!: AuditSeverity | AuditEventSeverity;

  @Column({ type: "boolean", default: true })
  success!: boolean;

  @Column({ type: "text", nullable: true })
  errorMessage?: string;

  // ── Request Context ────────────────────────────────────────────────────────

  @Column({ type: "varchar", nullable: true })
  ipAddress?: string;

  @Column({ type: "varchar", nullable: true })
  userAgent?: string;

  /** HTTP method + path that triggered the event, e.g. "POST /api/trade" */
  @Column({ type: "varchar", nullable: true })
  resource?: string;

  // ── Metadata (PII-scrubbed) ────────────────────────────────────────────────

  /** Arbitrary event context — automatically scrubbed for PII before persistence */
  @Column({ type: "jsonb", nullable: true })
  metadata?: Record<string, unknown>;

  // ── Tamper-Evidence ────────────────────────────────────────────────────────

  /**
   * SHA-256 hex digest of this event's immutable canonical fields.
   * Verifying the hash detects any post-write row mutation.
   */
  @Column({ type: "varchar", length: 64, nullable: true })
  eventHash?: string;

  /**
   * SHA-256 hex digest of the immediately preceding event.
   * Forms a linked hash-chain; a broken chain signals tampering.
   */
  @Column({ type: "varchar", length: 64, nullable: true })
  previousHash?: string;

  // ── Timestamps ─────────────────────────────────────────────────────────────

  @CreateDateColumn()
  @Index("idx_audit_created_at")
  createdAt!: Date;
}
