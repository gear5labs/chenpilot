/**
 * auditLog.entity.ts  (Issue #344 — Security-Grade Audit Ledger)
 *
 * TypeORM entity for the `audit_log` table, extended with:
 *   - correlationId   — distributed trace identifier
 *   - actorId / actorServiceId — canonical "who" fields
 *   - category        — event taxonomy bucket (Auth/Admin/Execution/Policy/Integration)
 *   - eventHash       — SHA-256 hash of immutable event fields (tamper-evidence)
 *   - previousHash    — SHA-256 hash of the preceding entry (chain integrity)
 *
 * Legacy AuditAction / AuditSeverity enums are preserved for backward-compat.
 * New code should use the typed enums from auditEvent.types.ts.
 */

import {
  Entity,
  PrimaryGeneratedColumn,
  Column,
  CreateDateColumn,
  Index,
} from "typeorm";
import { EventCategory, AuditEventSeverity } from "./auditEvent.types";

